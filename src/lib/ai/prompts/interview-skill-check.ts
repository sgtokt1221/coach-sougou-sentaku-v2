/**
 * 音声解析（話速・フィラー等）のサマリを評価プロンプトに注入する場合のヘルパ
 */
export function appendVoiceAnalysisToEvaluation(
  base: string,
  voiceSummary?: string,
): string {
  if (!voiceSummary) return base;
  return `${base}\n\n# 音声解析データ（demeanor採点時に加味すること）\n${voiceSummary}`;
}
