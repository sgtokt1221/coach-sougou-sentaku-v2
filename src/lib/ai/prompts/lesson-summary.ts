/**
 * 成長レポート生成時、期間内の授業メモ (debrief.notes 連結) から
 * 2-3 件のキーポイントを抽出するためのプロンプト
 */

export function buildLessonObservationSummaryPrompt(
  studentName: string,
  concatenatedNotes: string,
): string {
  return `あなたは総合型選抜対策塾の塾長です。${studentName} さんの期間内の授業で、講師が残した観察メモを読み、保護者・生徒本人が読んでも意味の通る「授業を通じた気づき」を 2-3 件抽出してください。

## 出力形式
JSON 配列形式の文字列リスト。1 件あたり 30-60 字。
\`\`\`json
["気づき1の本文", "気づき2の本文", "気づき3の本文"]
\`\`\`

## 抽出方針
- 成長の兆し / 取り組み姿勢の変化 / 新しく見えた強みや課題を優先
- 愚痴・授業進行上のメモ (「時間が足りなかった」等) は除外
- 生徒個人の赤裸々なエピソードは抽象化
- 同じ趣旨の記述は 1 件にまとめる

## 講師の観察メモ (期間内の全授業を連結)
${concatenatedNotes.slice(0, 4000)}

JSON 配列のみを出力してください。前後に説明文を入れない。`;
}
