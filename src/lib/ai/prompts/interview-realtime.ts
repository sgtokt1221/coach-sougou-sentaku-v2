export function buildRealtimeGdSpeakerInstructions(
  speaker: GdSpeakerKey,
  universityName: string,
  facultyName: string,
  admissionPolicy: string,
  weaknessList: string,
  interviewTendency?: InterviewTendency,
): string {
  const characterPrompt = GD_CHARACTER_PROMPTS[speaker];

  return `${characterPrompt}

## 討論の文脈
${universityName} ${facultyName} の入学試験における集団討論です。

## アドミッションポリシー (必ず意識すること)
${admissionPolicy}

この AP は討論の背景に常にあります。自分の発言や質問は必ず AP の価値観と矛盾しないように、むしろ AP の重点領域に議論が向かうように組み立ててください。
${interviewTendency ? `\n## この学部の面接傾向\n${typeof interviewTendency === "string" ? interviewTendency : JSON.stringify(interviewTendency)}` : ""}

## あなたが特に意識する受験生Dさん (ユーザー) の弱点
${weaknessList}

## 絶対ルール
- 日本語で話す
- 自分のキャラクターだけを演じる。他のキャラクターを代弁しない
- 発言は短く (1-3 文)
- 相槌だけの発言は禁止。必ず立場や理由を添える
- 受験生Dさんを指すときは「受験生Dさん」または「あなた」
- 集団討論である以上、他の参加者の名前を呼んで議論する

## 傾聴の姿勢 (重要)
- **反論するときでも必ず「〇〇さんの意見は分かります」「その視点は興味深いですね」など一度受け止めてから自分の立場を述べる**
- 「いえ」「違います」「でも」のような否定語から発言を始めるのは NG
- 相手の発言内容を自分の言葉で要約してから補強・反論する (アクティブリスニング)
- ユーザー (受験生Dさん) の発言に対しても必ず同じ姿勢で臨む。相手を否定から入らないこと
`;
}

function buildSelfAnalysisSection(sa: SelfAnalysisContext): string {
  const parts: string[] = [
    "## 受験生の自己分析結果 (面接で深掘りすべき素材)",
    "以下は受験生が事前の自己分析で明らかにした内容です。この情報を踏まえて、受験生の人物像をより深く理解した上で質問を組み立ててください。表面的な確認ではなく、自己分析の内容と AP の整合性を鋭く掘り下げてください。",
  ];
  if (sa.values?.length) {
    parts.push(`- 大切にしている価値観: ${sa.values.join("、")}`);
  }
  if (sa.strengths?.length) {
    parts.push(`- 強み: ${sa.strengths.join("、")}`);
  }
  if (sa.vision) {
    parts.push(`- 将来の理想像: ${sa.vision}`);
  }
  if (sa.selfStatement) {
    parts.push(`- 自己紹介文: ${sa.selfStatement}`);
  }
  parts.push("");
  return parts.join("\n");
}
