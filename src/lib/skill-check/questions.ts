export function getQuestionsByCategory(category: AcademicCategory): SkillCheckQuestion[] {
  return SKILL_CHECK_QUESTIONS.filter((q) => q.category === category);
}

export function pickRandomQuestion(
  category: AcademicCategory,
  excludeIds: string[] = [],
): SkillCheckQuestion | null {
  const pool = getQuestionsByCategory(category).filter((q) => !excludeIds.includes(q.id));
  const candidates = pool.length > 0 ? pool : getQuestionsByCategory(category);
  if (candidates.length === 0) return null;
  const idx = Math.floor(Math.random() * candidates.length);
  return candidates[idx];
}

export function getQuestionById(id: string): SkillCheckQuestion | null {
  return SKILL_CHECK_QUESTIONS.find((q) => q.id === id) ?? null;
}
